/// 盤面の1マスの状態。
///
/// `Empty` はまだコマが置かれていないマス、
/// `White` と `Black` はそれぞれ白・黒のコマが置かれているマスを表す。
/// メモにあった `0, 1, 2` のような数値表現は、保存や高速化の段階で
/// 別途変換関数として用意する方針にする。
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Cell {
    /// コマが置かれていない空きマス。
    Empty,

    /// 白のコマが置かれているマス。
    White,

    /// 黒のコマが置かれているマス。
    Black,
}
